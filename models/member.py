class Member:
    def __init__(self,first_name,last_name,date_of_birth,gender,notes,contact_info_email,contact_info_number,membership_type,id = None ):
        self.first_name = first_name
        self.last_name = last_name
        self.date_of_birth = date_of_birth
        self.gender = gender 
        self.notes = notes 
        self.contact_info_email = contact_info_email
        self.contact_info_number = contact_info_number
        self.membership_type = membership_type
        self.id = id 



